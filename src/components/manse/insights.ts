export function sajuPersonalInsight(name: string, dayStem: string): string {
  const element = STEM_ELEMENT[dayStem];
  const trait = element ? ELEMENT_TRAITS[element] : '';
  if (!element) return '';
  return `${name}님의 일간(자기 자신)은 ${dayStem}(${hanjaToHangul[dayStem] ?? ''}) — ${element} 기운입니다. ${trait}.`;
}

