export function fiveElementsPersonalInsight(
  chartData: { name: string; value: number }[]
): string {
  if (chartData.length === 0) return '';
  const sorted = [...chartData].sort((a, b) => b.value - a.value);
  const total = chartData.reduce((acc, d) => acc + d.value, 0);
  const top = sorted[0];
  const bottom = sorted[sorted.length - 1];
  const missing = ['목(木)', '화(火)', '토(土)', '금(金)', '수(水)'].filter(
    (k) => !chartData.find((d) => d.name === k),
  );
  const topShort = top.name.replace(/\([^)]+\)/, '');
  const topPercent = Math.round((top.value / total) * 100);
  let text = `당신의 사주는 ${topShort} 기운이 ${top.value}개(${topPercent}%)로 가장 많고`;
  if (sorted.length > 1) {
    const bottomShort = bottom.name.replace(/\([^)]+\)/, '');
    text += `, ${bottomShort} 기운이 ${bottom.value}개로 가장 적습니다`;
  }
  text += '. ';
  if (missing.length > 0) {
    const missShort = missing.map((m) => m.replace(/\([^)]+\)/, '')).join('·');
    text += `${missShort} 오행이 원국에 없으므로, 해당 영역은 의식적으로 보충하면 균형이 살아납니다.`;
  } else {
    text += '다섯 오행이 모두 갖춰져 있어 어떤 환경에서도 적응할 수 있는 사주입니다.';
  }
  return text;
}

